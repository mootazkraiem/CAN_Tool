THEME_DARK = "Dark Theme"
THEME_LIGHT = "Light Theme"

THEME_CHOICES = [THEME_DARK, THEME_LIGHT]

THEME_PALETTES = {
    THEME_DARK: DARK_THEME_PALETTE,
    THEME_LIGHT: LIGHT_THEME_PALETTE,
}


def normalize_theme_name(theme_name: str) -> str:
    if theme_name in THEME_PALETTES:
        return theme_name
    lowered = (theme_name or "").strip().lower()
    if "carbon" in lowered or "dark" in lowered:
        return THEME_DARK
    if "light" in lowered:
        return THEME_LIGHT
    return THEME_DARK


def get_theme_palette(theme_name: str):
    return THEME_PALETTES[normalize_theme_name(theme_name)]


def app_font() -> QFont:
    font = QFont(FONT_FAMILY_UI, FONT_SIZE_LARGE)
    font.setStyleStrategy(QFont.PreferAntialias)
    return font


def mono_font(point_size: int = FONT_SIZE_MEDIUM, weight: int = QFont.Normal) -> QFont:
    font = QFont(FONT_FAMILY_MONO, point_size, weight)
    font.setStyleHint(QFont.TypeWriter)
    font.setStyleStrategy(QFont.PreferAntialias)
    return font
